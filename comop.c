#include<stdio.h>
void main()
{
int no1,no2;
printf("Enter two nos:");
scanf("%d %d",&no1,&no2);

printf("\n Relation operations Result\n");
printf("no1==no2:%d\n",no1==no2);//equal to
printf("no1!=no2:%d\n",no1!=no2);	//not equal to
printf("no1>no2:%d\n",no1>no2);	//grater than
printf("no1<no2:%d\n",no1<no2);	//less than
printf("no1>=no2:%d\n",no1>=no2);	//grater than equal to
printf("no1<=no2:%d\n",no1<=no2);	//less than equal to

	
}
